SELECT MONTH(START_DATE) as MONTH, CAR_ID, COUNT(*) as RECORDS
FROM CAR_RENTAL_COMPANY_RENTAL_HISTORY 
WHERE START_DATE BETWEEN '2022-08-01 00:00' AND '2022-11-01 00:00'
    AND CAR_ID IN(
        SELECT CAR_ID
        FROM CAR_RENTAL_COMPANY_RENTAL_HISTORY
        WHERE START_DATE BETWEEN '2022-08-01 00:00' AND '2022-11-01 00:00'
        GROUP BY CAR_ID
        HAVING COUNT(*) >= 5
    )
GROUP BY  DATE_FORMAT(START_DATE, '%c'), CAR_ID
ORDER BY MONTH ASC, CAR_ID DESC;